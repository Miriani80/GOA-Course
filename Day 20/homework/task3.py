#3) 1-დან 100-მდე დაითვალეთ ხუთის ჯერადი რიცხვების ჯამი გამოიყენეთ if, else გამოგადგებათ % ნიშანი


# ხუთის ჯერადი რიცხვების ჯამის ინიციალიზაცია
sum_of_multiples = 0

# ციკლი 1-დან 100-მდე
for num in range(1, 101):
    if num % 5 == 0:
        sum_of_multiples += num

# შედეგის ბეჭდვა
print(f"ხუთის ჯერადი რიცხვების ჯამი: {sum_of_multiples}")

