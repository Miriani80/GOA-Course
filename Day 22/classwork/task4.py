#4)მომხამრებელს შევეკითხოთ სახელი და იქამდე არ შემოვუშვათ სახლში სანამ ის არიტყვის რომ ქვია სვარჩიკა მაყვალა


# დავიწყოთ while ციკლი
while True:
    # მომხმარებელს ვეკითხებით სახელს
    name = input("რა გქვია? ")

    # ვამოწმებთ, არის თუ არა სახელი 'სვარჩიკა მაყვალა'
    if name == "სვარჩიკა მაყვალა":
        print("კეთილი იყოს შენი მობრძანება!")
        break  # ციკლის შეწყვეტა
    else:
        print("შენ არ ხარ სვარჩიკა მაყვალა, ვერ შემოვალ სახლში.")
