#5)მომხმარებელს შეეკითხეთ ექაუნთძე შესასვლელი პაროლი, სანამ ის არ შემოიტანს სწორ პაროლს მას ხელახლა გაუმეორეთ რომ შემოიტანოს პაროლი თუ სწორად შემოიტანს დაბეჭდოს რომ ექაუნთზე შევიდა


# განსაზღვრეთ სწორი პაროლი
correct_password = "1234"

# დავიწყოთ while ციკლი
while True:
    # მომხმარებელს ვეკითხებით პაროლს
    password = input("გთხოვთ, შეიყვანეთ პაროლი: ")

    # ვამოწმებთ, სწორად შემოიტანა თუ არა პაროლი
    if password == correct_password:
        print("ექაუნთზე წარმატებით შეხვედით!")
        break  # ციკლის შეწყვეტა
    else:
        print("არასწორი პაროლი, სცადეთ კიდევ ერთხელ.")

