#2) მომხმარებელს შემოატანინეთ რიცხვი და შემდეგ for loop - ის გამოყენებით გამოიტანეთ ამ რიცხვამდე ყველა რიცხვის ჯამი(მაგალითად თუ შეიყვანს 10ს გამოიტანეთ 10მდე ყველა რიცხვის ჯამი მაგალითად


# მომხმარებლის მიერ შეყვანილი რიცხვის მიღება
n = int(input("შეიყვანეთ რიცხვი: "))

# ჯამი მისაღები რიცხვების
sum_total = 0

# for ციკლის გამოყენება ჯამის გასათვლელად
for i in range(1, n + 1):
    sum_total += i

# შედეგის გამოყვანა
print(f"{n} რიცხვამდე ყველა რიცხვის ჯამი არის: {sum_total}")
