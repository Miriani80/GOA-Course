# განმეორებით სთხოვეთ მომხმარებელს სახელი, სანამ "quit" არ შეიყვანს
while True:
    name = input("შეიყვანეთ თქვენი სახელი (თუ გსურთ პროგრამის შეწყვეტა, ჩაწერეთ 'quit'): ")
    
    if name.lower() == "quit":
        print("პროგრამა დასრულდა.")
        break
    else:
        print(f"თქვენი სახელი არის: {name}")






