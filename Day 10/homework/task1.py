def opposite_sign(number):
    if number > 0:
        return -number
    elif number < 0:
        return -number
    else:
        return 0

# საცდელი რიცხვები
numbers = [10, -5, 0, 23, -7]

for num in numbers:
    print(f'Original number: {num}, Opposite sign: {opposite_sign(num)}')